public class converterWeight {


    public static double gramsToKilograms(double grams) {  //convert from grams to kilograms

        return grams/1000;
    }

    public static double gramsToPounds(double grams) {
        return grams / 453.592;
    }

    public static double kilogramsToGrams(double kilograms) {  //convert from kilograms to grams

        return kilograms * 1000;
    }

    public static double poundsToGrams(double pounds) {
        return pounds * 453.592;
    }

    public static double poundsToKilograms(double pounds) {   //convert from pound to kilograms

        return pounds/2.2046;
    }

    public static double kilogramsToPounds(double kilograms) {    //convert from kilograms to pounds

        return kilograms * 2.2046;
    }

    public static double ouncesToPounds(double ounces) {    //convert from ounces to pounds
        return ounces/35.274;
    }

    public static double poundsToOunces(double pounds) {   //convert from pounds to ounces

        return pounds * 35.274;
    }

    public static double ouncesToGrams(double ounces) {  //convert from ounces to grams

        return ounces/28.3495;
    }

    public static double gramsToOunces(double grams) {   //convert from grams to ounces

        return grams * 28.3495;
    }

    public static double ouncesToKilograms(double ounces) {   //convert from ounces to kilograms

        return ounces/35.274;
    }

    public static double kilogramsToOunces(double kilograms) {   //convert from kilograms to ounces

        return kilograms * 35.274;
    }


}
