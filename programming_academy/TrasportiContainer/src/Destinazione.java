public class Destinazione {
    private String CittàDest;

    public Destinazione(String CittàDest) {
        this.CittàDest = CittàDest;
    }
    public String getDestinazione() {
        return CittàDest;
    }
    public String setDestinazione(String CittàDest) {
        this.CittàDest = CittàDest;
        return this.CittàDest;
    }
}
